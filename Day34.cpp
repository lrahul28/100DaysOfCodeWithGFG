class Solution{
public:
    Node* divide(int N, Node *head){
      Node *e, *o;
      e = new Node(0);
      o = new Node(0);
      Node *ohead = o;
      Node *ehead = e;
      Node *curr = head;
      while(curr)
      {
          if(curr -> data % 2 == 0)
          {
              e -> next = curr;
              e = e -> next;
          }
          else
          {
              o -> next = curr;
              o = o -> next;
          }
          curr = curr -> next;
      }
      o -> next = NULL;
      e -> next = ohead -> next;
      return ehead -> next;
    }
};

